import UIKit

class AppFonts {
    
    
    static func Regular (size : CGFloat) -> UIFont {
        return  UIFont(name: "NeoSansW23", size: size)!
    }
    
    static func medium (size : CGFloat) -> UIFont {
        return  UIFont(name: "NeoSansW23", size: size)!
    }
    
    
}
